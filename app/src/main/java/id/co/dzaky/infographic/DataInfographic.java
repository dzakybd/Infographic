public class DataInfographic {

    private String idinfographic;
    private String name;
    private String image;
    private String likes;
    private String updateat;

    /**
     *
     * @return
     * The idinfographic
     */
    public String getIdinfographic() {
        return idinfographic;
    }

    /**
     *
     * @param idinfographic
     * The idinfographic
     */
    public void setIdinfographic(String idinfographic) {
        this.idinfographic = idinfographic;
    }

    /**
     *
     * @return
     * The name
     */
    public String getName() {
        return name;
    }

    /**
     *
     * @param name
     * The name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     *
     * @return
     * The image
     */
    public String getImage() {
        return image;
    }

    /**
     *
     * @param image
     * The image
     */
    public void setImage(String image) {
        this.image = image;
    }

    /**
     *
     * @return
     * The likes
     */
    public String getLikes() {
        return likes;
    }

    /**
     *
     * @param likes
     * The likes
     */
    public void setLikes(String likes) {
        this.likes = likes;
    }

    /**
     *
     * @return
     * The updateat
     */
    public String getUpdateat() {
        return updateat;
    }

    /**
     *
     * @param updateat
     * The updateat
     */
    public void setUpdateat(String updateat) {
        this.updateat = updateat;
    }

}